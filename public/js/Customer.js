class Customer {

    setFirstName(firstName){
        this.firstName = firstName;
    }
    setLastName(lastName){
        this.lastName = lastName;
    }
    setAddress(address){
        this.address = address;
    }
    setCity(city){
        this.city = city;
    }
    setEmail(email){
        this.email = email;
    }

    getFirstName(){
        return this.firstName;
    }
    getLastName(){
        return this.lastName;
    }
    getAddress(){
        return this.address;
    }
    getCity(){
        return this.city;
    }
    getEmail(){
        return this.email;
    }
}