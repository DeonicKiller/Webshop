class Product{

    setName(name){
        this.name = name;
    }
    setPrice(price){
        this.price = price;
    }
    setPlatform(platform){
        this.platform = platform;
    }
    setImage(image){
        this.image = image;
    }

    getName(){
        return this.name;
    }
    getPrice(){
        return this.price;
    }
    getPlatform(){
        return this.platform;
    }
    getImage(){
        return this.image;
    }

    

    

}