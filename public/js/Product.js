class Product{
    
    setId(id){
        this.id = id;

    }
    
    setName(name){
        this.name = name;
    }
    setPrice(price){
        this.price = price;
    }
    setPlatform(platform){
        this.platform = platform;
    }
    setImage(image){
        this.image = image;
    }
    setDescription(description){
        this.description = description;

    }
    getId(){
        return this.id;
    }

    getName(){
        return this.name;
    }
    getPrice(){
        return this.price;
    }
    getPlatform(){
        return this.platform;
    }
    getImage(){
        return this.image;
    }
    getDescription(){
        return this.description;
    }

    

    

}